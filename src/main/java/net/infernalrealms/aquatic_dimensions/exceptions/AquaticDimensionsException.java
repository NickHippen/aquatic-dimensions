package net.infernalrealms.aquatic_dimensions.exceptions;

public class AquaticDimensionsException extends Exception {
	
	public AquaticDimensionsException() {
	}
	
	public AquaticDimensionsException(String message) {
		super(message);
	}
	
	public AquaticDimensionsException(Throwable throwable) {
		super(throwable);
	}
	
	public AquaticDimensionsException(String message, Throwable throwable) {
		super(message, throwable);
	}

}
