package net.infernalrealms.aquatic_dimensions.mobs;

public class MonsterEquipment {

	private String helmet;
	private String chestplate;
	private String leggings;
	private String boots;
	private String mainHand;
	private String offHand;

	public String getHelmet() {
		return helmet;
	}

	public void setHelmet(String helmet) {
		this.helmet = helmet;
	}

	public String getChestplate() {
		return chestplate;
	}

	public void setChestplate(String chestplate) {
		this.chestplate = chestplate;
	}

	public String getLeggings() {
		return leggings;
	}

	public void setLeggings(String leggings) {
		this.leggings = leggings;
	}

	public String getBoots() {
		return boots;
	}

	public void setBoots(String boots) {
		this.boots = boots;
	}

	public String getMainHand() {
		return mainHand;
	}

	public void setMainHand(String mainHand) {
		this.mainHand = mainHand;
	}

	public String getOffHand() {
		return offHand;
	}

	public void setOffHand(String offHand) {
		this.offHand = offHand;
	}

}
